//Como utilizar o WHILE

public class Aula22 {
    public static void main(String[] args) {

        int i = 1;
        while(i < 101){
            System.out.println(i);
            ++i;
        }
    }
}

// WHILE significa enquanto

//Enquanto a condição do while for verdadeira, eu quero que execute o que está dentro dos ().
