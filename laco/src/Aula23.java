public class Aula23 {
    public static void main(String[] args) {

        int i = 1;
        int j = 1;
        do{
            System.out.println(i);
            ++i;
        }while(i < 20);

        while(i < 20){
            System.out.println(i);
            ++j;
        }
    }
}

//do = faça

//while = enquanto


// Diferença entre WHILE e DO WHILE

// O DO WHILE garante ao menos uma vez a execução do ciclo.
