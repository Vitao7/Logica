// Ciclo FOR
public class Aula24 {
    public static void main(String[] args) {

        for(int i = 0, j =0 ; i < 100 && j < 50; ++i, ++j){
            System.out.println(i);
        }
    }
}

//Primeira parte do for é colocar uma variavel e o valor dela.

//Depois temos a nossa condição.

//Depois colocar o incremento.

//Incremento, toda vez que meu ciclo rodar, eu adiciono um valor a variavel

